class Funciones:
    
    def __init__(self, n1 , n2):
        Funciones.n1 = n1
        Funciones.n2 = n2

    def sumar(self):
        print(f"\nLa suma es {self.n1+self.n2}")
    
    def multiplicacion(self):
        print(f"\nEl producto entre {self.n1*self.n2}")

    def division(self):

        print(f"\nEl cosciente de la division es {self.n1/self.n2}.")
